# Пользователь вводит время в секундах. Рассчитайте время и сохраните отдельно в каждую переменную количество часов, минут и секунд.
# Переведите время в часы, минуты, секунды и сохраните в отдельных переменных.
#Используйте приведение типов для перевода строк в числовые типы. Предусмотрите проверку строки на наличие только числовых данных через
# встроенный строковый метод .isdigit(). Выведите рассчитанные часы, минуты и секунды по отдельности в консоль.
user_input = input("Введите время в секундах: ")

# Проверяем, что введены только числовые данные
if user_input.isdigit():
    # Преобразуем строку в целое число
    total_seconds = int(user_input)

    # Вычисляем часы, минуты и секунды
    hours = total_seconds // 3600  # Количество полных часов
    minutes = (total_seconds % 3600) // 60  # Количество полных минут
    seconds = total_seconds % 60  # Оставшиеся секунды

    # Выводим результат
    print(f"Часы: {hours}")
    print(f"Минуты: {minutes}")
    print(f"Секунды: {seconds}")
else:
    # Если введены некорректные данные
    print("Ошибка: Введите только числовые данные!")



# 3.    Запросите у пользователя через консоль число n (от 1 до 9). Найдите сумму чисел n + nn + nnn.
#
# Например, пользователь ввёл число 3. Считаем 3 + 33 + 333 = 369.
#
# Выведите данные в консоль.
user_input = input("Введите число n (от 1 до 9): ")

# Проверяем, что введено только одно число от 1 до 9
if user_input.isdigit() and len(user_input) == 1 and 1 <= int(user_input) <= 9:
    # Преобразуем строку в число
    n = int(user_input)

    # Формируем числа nn и nnn
    nn = int(f"{n}{n}")  # Например, если n = 3, то nn = 33
    nnn = int(f"{n}{n}{n}")  # Например, если n = 3, то nnn = 333

    # Вычисляем сумму
    result = n + nn + nnn

    # Выводим результат
    print(f"Сумма чисел {n} + {nn} + {nnn} = {result}")
else:
    # Если введены некорректные данные
    print("Ошибка: Введите число от 1 до 9!")

